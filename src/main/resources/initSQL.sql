    chog2 decimal(6,2),
    chog4 decimal(6,2),
    chog56 decimal(6,2),
    chog8 decimal(6,2),
    chog112 decimal(6,2),
    chog16 decimal(6,2),
    chog224 decimal(6,2),
    chog315 decimal(6,2),
    gmb decimal(4,3),
    gmm decimal(4,3)
);

